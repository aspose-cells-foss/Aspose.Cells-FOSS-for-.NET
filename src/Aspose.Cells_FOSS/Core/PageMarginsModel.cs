using System.Collections.Generic;

namespace Aspose.Cells_FOSS.Core;

/// <summary>
/// Represents page margins model.
/// </summary>
public sealed class PageMarginsModel
{
    /// <summary>
    /// Gets or sets the left.
    /// </summary>
    public double Left { get; set; } = 0.7d;
    /// <summary>
    /// Gets or sets the right.
    /// </summary>
    public double Right { get; set; } = 0.7d;
    /// <summary>
    /// Gets or sets the top.
    /// </summary>
    public double Top { get; set; } = 0.75d;
    /// <summary>
    /// Gets or sets the bottom.
    /// </summary>
    public double Bottom { get; set; } = 0.75d;
    /// <summary>
    /// Gets or sets the header.
    /// </summary>
    public double Header { get; set; } = 0.3d;
    /// <summary>
    /// Gets or sets the footer.
    /// </summary>
    public double Footer { get; set; } = 0.3d;
}
